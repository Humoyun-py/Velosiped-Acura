// Current language
let currentLang = localStorage.getItem('preferred_language') || 'uz';

// Initialize language
function initLanguage() {
    // Set active language button
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.remove('active');
        if (btn.dataset.lang === currentLang) {
            btn.classList.add('active');
        }
        
        // Add click event
        btn.addEventListener('click', function() {
            currentLang = this.dataset.lang;
            localStorage.setItem('preferred_language', currentLang);
            translatePage();
            
            // Update active button
            document.querySelectorAll('.lang-btn').forEach(b => b.classList.remove('active'));
            this.classList.add('active');
        });
    });
    
    // Translate page
    translatePage();
}

// Translate the page
function translatePage() {
    // Translate all elements with data-translate attribute
    document.querySelectorAll('[data-translate]').forEach(element => {
        const key = element.getAttribute('data-translate');
        if (translations[currentLang] && translations[currentLang][key]) {
            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                const placeholderKey = element.getAttribute('data-placeholder-' + currentLang);
                if (placeholderKey) {
                    element.placeholder = placeholderKey;
                }
            } else if (element.tagName === 'OPTION') {
                element.textContent = translations[currentLang][key];
            } else {
                element.textContent = translations[currentLang][key];
            }
        }
    });
    
    // Update page title if needed
    const titleElement = document.querySelector('title[data-translate]');
    if (titleElement) {
        const key = titleElement.getAttribute('data-translate');
        if (translations[currentLang] && translations[currentLang][key]) {
            document.title = translations[currentLang][key] + ' | PremiumShop';
        }
    }
}

// Initialize when DOM is loaded
document.addEventListener('DOMContentLoaded', initLanguage);